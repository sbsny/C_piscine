int	ft_str_is_alpha(char *str)
{
	char	a;
	int		i;

	i = 0;
	while (str[i])
	{
		a = str[i];
		if (!((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z')))
			return (0);
		i++;
	}
	return (1);
}
