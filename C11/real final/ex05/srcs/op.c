int	remainde(int i, int j)
{
	return (i % j);
}

int	sub(int i, int j)
{
	return (i - j);
}

int	add(int i, int j)
{
	return (i + j);
}

int	div(int i, int j)
{
	return (i / j);
}

int	mul(int i, int j)
{
	return (i * j);
}
